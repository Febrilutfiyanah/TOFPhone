    private void txt_produkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_produkActionPerformed
        try {
            String kode = txt_produk.getText().trim();
            if (kode.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Kode produk tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Connection K = koneksi.konek();
            String Q = "SELECT * FROM produk WHERE kode_produk = ?";
            PreparedStatement PS = K.prepareStatement(Q);
            PS.setString(1, kode);
            ResultSet R = PS.executeQuery();

            if (R.next()) {
                int id = R.getInt("id");
                String nama = R.getString("nama_produk");
                int harga_jual = R.getInt("harga_jual");

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                boolean ada = false;
                int rowCount = jTable1.getRowCount();

                for (int i = 0; i < rowCount; i++) {
                    Object value = jTable1.getValueAt(i, 0);
                    if (value != null) {
                        int id_produk = Integer.parseInt(value.toString());
                        if (id_produk == id) {
                            int QTY = Integer.parseInt(jTable1.getValueAt(i, 2).toString()) + 1;
                            jTable1.setValueAt(QTY, i, 2);
                            ada = true;
                            break;
                        }
                    } else {
                        System.out.println("Kolom ID kosong pada baris " + i);
                    }
                }

                if (!ada) {
                    Object[] data = {id, nama, 1, harga_jual};
                    model.addRow(data);
                }

                updateTotalHarga();
                endsKetikUangPembayaran();
            } else {
                JOptionPane.showMessageDialog(this, "Produk dengan kode " + kode + " tidak ditemukan!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Format data tidak valid!", "Data Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txt_produkActionPerformed

